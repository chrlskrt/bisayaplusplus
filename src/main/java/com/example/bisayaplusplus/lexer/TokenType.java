package com.example.bisayaplusplus.lexer;

public enum TokenType {
// single char
    LEFT_PAREN, RIGHT_PAREN, LEFT_BRACKET, RIGHT_BRACKET,
    ASTERISK, DIVIDE, MODULO, PLUS, MINUS,
    GREATER_THAN, LESSER_THAN, EQUAL,
    UNDERSCORE, AMPERSAND, HASHTAG,

// double char
    GREATER_OR_EQUAL, LESSER_OR_EQUAL,
    DOUBLE_EQUAL, NOT_EQUAL, DOUBLE_MINUS,

// data types
    NUMERO, LETRA, TINUOD, TIPIK,

// logical
    UG, O, DILI,

// boolean
    OO, DILIHAAAAAAunsaonyrros,
}
